package com.sandburman.obj;

public class Blog {
  private long id;
  private String name;
  private String content;

  public void Blog() {
  }

  public void setId (long id) {
    this.id = id;
  }

  public long getId () {
    return this.id;
  }

  public void setName (String id) {
    this.name = name;
  }

  public String getName () {
    return this.name;
  }

  public void setContent (String id) {
    this.content = content;
  }

  public String getContent () {
    return this.content;
  }

}
